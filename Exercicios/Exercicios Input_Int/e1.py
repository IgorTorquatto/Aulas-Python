"""
Faça um Programa que peça as 4 notas bimestrais e mostre a média do aluno
"""
x=float(input("Digite a nota 1 do aluno: "))
y=float(input("Digite a nota 2 do aluno: "))
z=float(input("Digite a nota 3 do aluno: "))
w=float(input("Digite a nota 4 do aluno: "))
media=(x+y+z+w)/4.0
print("A média do aluno é: ",media)